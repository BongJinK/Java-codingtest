package section6;

import java.io.BufferedReader;
import java.io.IOException;
import java.io.InputStreamReader;
import java.util.Arrays;

public class Main6 {
	// 6. 장난꾸러기
	// 철수네 반에는 N명의 학생들이 있습니다.
	// 반 번호를 정해 주기 위해 반 학생들을 키가 가장 작은 학생부터 일렬로 키순으로 세웠습니다.
	// 제일 앞에 가장 작은 학생부터 반 번호를 1번부터 N번까지 부여합니다. 철수는 짝꿍보다 키가 큽니다.
	// 그런데 철수가 앞 번호를 받고 싶어 짝꿍과 자리를 바꿨습니다.
	// 선생님은 이 사실을 모르고 학생들에게 서있는 순서대로 번호를 부여했습니다.
	// 철수와 짝꿍이 자리를 바꾼 반 학생들의 일렬로 서있는 키 정보가 주어질 때
	// 철수가 받은 번호와 철수 짝꿍이 받은 번호를 차례로 출력하는 프로그램을 작성하세요.

	// 첫 번째 줄에 자연수 N(5<=N<=100)이 주어진다.
	// 두 번째 줄에 제일 앞에부터 일렬로 서있는 학생들의 키가 주어진다.
	// 키(높이) 값 H는 (120<=H<=180)의 자연수 입니다.
	// 첫 번째 줄에 철수의 반 번호와 짝꿍의 반 번호를 차례로 출력합니다.

	// 9
	// 120 125 152 130 135 135 143 127 160
	// 3 8
	
	// 깊은 복사 : '실제 값'을 새로운 메모리 공간에 복사 하는 것
	// 얕은 복사 : '주소 값'을 복사

	public static String sol(int n, int[] arr) {
		StringBuffer str = new StringBuffer("");
		int[] temp = arr.clone();
		Arrays.sort(temp);		

		for (int i = 0; i < n; i++) {
			if (arr[i] != temp[i])
				str.append(i + 1 + " ");
		}
		return str.toString();
	}

	public static void main(String[] args) throws IOException {
		BufferedReader br = new BufferedReader(new InputStreamReader(System.in));

		int n = Integer.parseInt(br.readLine());
		int[] arr = new int[n];
		String[] h = br.readLine().split(" ");

		for (int i = 0; i < n; i++)
			arr[i] = Integer.parseInt(h[i]);
		System.out.println(sol(n, arr));
	}
}
